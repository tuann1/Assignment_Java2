    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
        // TODO add your handling code here:
        Comparator<Manager> com = (Manager o1, Manager o2) -> o1.getId().compareTo(o2.getId());
        Collections.sort(listManager, com);
        listPaging = listManager;
        loadTable(getListByCurrentPage());
    }//GEN-LAST:event_btnSortActionPerformed

    void loadTable(List<Manager> list) {
        model.setRowCount(0);
        for (Manager man : list) {
            model.addRow(new Object[]{
                man.getId(), man.getName(), man.getTaskType(), man.getDate(), man.getPlanFrom(), man.getPlanTo(), man.getAssignee(), man.getReviewer(),
                man.getH()});
        }
    }

    List<Manager> getListByCurrentPage() {
        totalItem = listPaging.size();
        numPage = (totalItem - 1) / itemPerpage + 1;
        pageIndexLabel.setText(currentPage + "/" + numPage);
        int start = (currentPage - 1) * itemPerpage;
        int end = start + itemPerpage;
        if (end > totalItem) {
            end = totalItem;
        }
        return listPaging.subList(start, end);
    }

