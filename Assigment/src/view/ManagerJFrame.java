    Manager findManagerById(String id) {
        // tim kiem theo id
        for (Manager m : listManager) {
            if (m.getId().equalsIgnoreCase(id)) {
                return m;
            }
        }
        return null;
    }

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        boolean isValid = true;
        String id = txtID.getText();
        // 
        Manager findManager = findManagerById(id);

        if (id.isEmpty()) {
            msgID.setText("ID không được để trống.");
            msgID.setForeground(Color.red);
            isValid = false;
        } else {
            msgID.setText("");
        }

        if (findManager != null) {
            msgID.setText("ID này đã tồn tại.");
            msgID.setForeground(Color.red);
            isValid = false;
        }

//        try {
//                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
//                sdf.setLenient(false);
//                sdf.parse(date_TextField.getText());
//                date_Label.setText("");
//            } catch (Exception e) {
//                date_Label.setText("Nhap sai dinh dang !");
//                date_Label.setForeground(Color.red);
//            }
//        }
        
        String name = txtName.getText();
        String taskType = (String) cbxTaskType.getSelectedItem();
        String date = txtDate.getText();
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);
            sdf.parse(txtDate.getText());
            msgDate.setText("");
        } catch (ParseException e) {
            msgDate.setText("Nhập sai định dạng. Nhập lại!");
            msgDate.setForeground(Color.red);
            isValid = false;
        }

        double planFrom = -1;
        String str = "";
        try {
            planFrom = Double.parseDouble(txtPlanfrom.getText());
            msgPlanFrom.setText("");
            if (planFrom < 8 || planFrom > 9.5) {
                str = str + "Vui lòng nhập trong khoảng 8->9.5";
                isValid = false;
            }
            if (planFrom % 0.5 != 0) {
                str = str + ", Nhập sai định dạng.";
                isValid = false;
            }
            msgPlanFrom.setText(str);
            msgPlanFrom.setForeground(Color.red);

        } catch (NumberFormatException e) {
            msgPlanFrom.setText("Lỗi dữ liệu.");
            msgPlanFrom.setForeground(Color.red);
            isValid = false;
        }

        double planTo = -1;
        String str1 = "";
        try {
            planTo = Double.parseDouble(txtPlanTo.getText());
            msgPlanTo.setText("");
            if (planTo > 17.5 || planTo < planFrom) {
                str1 = str1 + "Plan To <= 17.5";
                isValid = false;
            }
            if (planTo % 0.5 != 0) {
                str1 = str1 + ", Nhập sai định dạng.";
                isValid = false;
            }

            msgPlanTo.setText(str1);
            msgPlanTo.setForeground(Color.red);
        } catch (NumberFormatException e) {
            msgPlanTo.setText("Lỗi dữ liệu.");
            msgPlanTo.setForeground(Color.red);
            isValid = false;
        }

        String assignee = txtAssignee.getText();
        String reivew = txtReviewer.getText();
        double h = planTo - planFrom;

        if (isValid == true) {
            Manager new_manager = new Manager(id, name, taskType, date, planFrom, planTo, assignee, reivew, h);
            listManager.add(new_manager);
            listPaging = listManager;
            currentPage = 1;
            JOptionPane.showMessageDialog(this, "Success");
            loadTable(getListByCurrentPage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

